magicians = ['alice', 'david', 'carolina']
for magician in magicians:
    print(magician)

# We begin by defining a list, just as we did in Chapter 3.
# Then we define a for loop. This line tells Python to pull a name from the list magicians,
# and associate it with the variable magician.
# Next, we tell Python to print the name that’s just been assigned to magician.
# Python then repeats these last two lines, once for each name in the list.
# It might help to read this code as “For every magician in the list of magicians, print the magician’s name.”

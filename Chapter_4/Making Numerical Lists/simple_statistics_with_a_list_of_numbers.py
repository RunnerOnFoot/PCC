# A few Python functions are helpful when working with lists of numbers.
# For example, you can easily find the minimum, maximum, and sum of a list of numbers:
digits = [1, 2, 3, 4, 5, 6, 7, 8, 9, 0]

# finding minimum
print(min(digits))

# finding maximum
print(max(digits))

# finding sum
print(sum(digits))

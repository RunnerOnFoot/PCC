cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)

# You can also sort this list in reverse-alphabetical order,
# by passing the argument reverse=True to the sort() method.
# The following example sorts the list of cars in reverse-alphabetical order:
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True)
print('reverse-alphabetical order:', cars)
